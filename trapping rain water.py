def trap(height):
    n = len(height)
    if n == 0:
        return 0

    leftMax = [0] * n
    rightMax = [0] * n

    # Fill leftMax array
    leftMax[0] = height[0]
    for i in range(1, n):
        leftMax[i] = max(leftMax[i-1], height[i])

    # Fill rightMax array
    rightMax[n-1] = height[n-1]
    for i in range(n-2, -1, -1):
        rightMax[i] = max(rightMax[i+1], height[i])

    water = 0
    for i in range(n):
        water += min(leftMax[i], rightMax[i]) - height[i]

    return water
